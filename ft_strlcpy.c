#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	while (src[i] && i < size)
	{
		dst[i] = src[i];
		if (i == size)
		{
			dst[i] = '\0';
			break ;
		}
		i++;
	}
	while (src[i])
		i++;
	return (i);
}
